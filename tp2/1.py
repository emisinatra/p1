#1-	Crear un programa que reciba el número de años que tiene nuestra computadora
# y muestre en la consola que el computador es nuevo si es menor o igual a 2 años
# y que el computador es viejo si es mayor a 2 años.
años = int(input("Ingrese el número de años que tiene su computadora: "))
if años <= 2:
    print("El computador es nuevo")
else:
    print("El computador es viejo")

